st.markdown("输入你队伍中 6 只精灵的属性，扫描全队**致命弱点**（全队均被克制）。")

# 使用侧边栏或主界面录入
team_attrs = []
cols = st.columns(2) # 分成两列，适配手机竖屏

for i in range(6):
    col_idx = i % 2
    with cols[col_idx]:
        st.subheader(f"精灵 {i+1}")
        attr1 = st.selectbox(f"属性 1", TYPES[1:], key=f"a1_{i}", index=0)
        attr2 = st.selectbox(f"属性 2", TYPES, key=f"a2_{i}", index=0)
        
        sprite = [attr1]
        if attr2 != "无":
            sprite.append(attr2)
        team_attrs.append(sprite)

# --- 4. 逻辑计算 ---
if st.button("🚀 开始分析致命盲点", use_container_width=True):
    st.divider()
    gaps = []
    
    # 遍历所有攻击属性
    for atk in TYPES[1:]:
        # 计算每一只精灵受到的伤害
        team_dmgs = []
        for sprite in team_attrs:
            m = 1.0
            for a in sprite:
                m *= DEFENSE_CHART[a].get(atk, 1.0)
            team_dmgs.append(m)
        
        # 寻找全队抗性最好的人（最小伤害）
        best_def = min(team_dmgs)
        
        # 只显示 2倍 及以上的盲点
        if best_def >= 2.0:
            gaps.append((atk, best_def))

    if gaps:
        st.error("### ⚠️ 发现致命盲点！")
        st.info("以下属性的攻击可以对你全队造成毁灭性打击（没有任何人能抗住）：")
        for atk, val in gaps:
            st.warning(f"**{atk}系**：全队最小承伤 **{val}x**")
    else:
        st.success("### ✅ 联防完美！")
        st.write("你的战队对所有属性都至少有一个成员能平接（1x）或抵抗（0.5x）。")

